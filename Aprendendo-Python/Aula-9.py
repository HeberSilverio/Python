import os

dia = int(input("Digite um de 1 a 7: "))

if dia == 1:
   print("domingo.")
elif dia == 2:
   print("segunda.")
elif dia == 3:
   print("terça-feira.")
elif dia == 4:
   print("quarta.")
elif dia == 5:
   print("quinta.")
elif dia == 6:
   print("sexta.")   
elif dia == 7:
   print("sabado.")
else:
   print("Digite um numero de 1 a 7 valido😁")
   
